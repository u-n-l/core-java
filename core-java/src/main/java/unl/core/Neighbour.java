package unl.core;

public class Neighbour {
    public String n;
    public String ne;
    public String e;
    public String se;
    public String s;
    public String sw;
    public String w;
    public String nw;

    public Neighbour(String n, String ne, String e, String se, String s, String sw, String w, String nw) {
        this.n = n;
        this.ne = ne;
        this.e = e;
        this.se = se;
        this.s = s;
        this.sw = sw;
        this.w = w;
        this.nw = nw;
    }

}
